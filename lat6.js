// do while

//Do akan melakukan perintahnya sekali baru mencari kondisi
//Sehingga outputnya walaupun i lebih dari 10 akan ada hello world 1x
let i = 20
do {
    i++
    console.log('Hallo Dunia Baru')
} while(i < 10)

